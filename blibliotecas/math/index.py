import math

# Raiz quadrada de um número
numero = 25
raiz = math.sqrt(numero)
print(f"A raiz quadrada de {numero} é {raiz}")

# Arredondar sempre para cima (ceil = teto)
preco = 10.20
print(f"O valor {preco} arredondado para cima é: {math.ceil(preco)}")

# Valor de PI (usado em cálculos de círculos)
print(f"O valor de PI é: {math.pi}")