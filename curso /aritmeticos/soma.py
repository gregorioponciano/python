n1 = 5 + 2    # Soma: resulta em 7
n2 = 5 - 2    # Subtração: resulta em 3
n3 = 5 * 2    # Multiplicação: resulta em 10
n4 = 5 / 2    # Divisão: resulta em 2.5
n5 = 5 ** 2   # Potenciação (5 elevado a 2): resulta em 25
n6 = 5 // 2   # Divisão Inteira (quociente): resulta em 2
n7 = 5 % 2    # Módulo (resto da divisão): resulta em 1

n8 = 5 + 3 * 2

